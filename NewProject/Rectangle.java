public class Rectangle {
	float length;
	float breadth;
	float areaval;
	
	public float getLength() {
		return length;
	}
	public void setLength(float length) {
		this.length = length;
	}
	public float getBreadth() {
		return breadth;
	}
	public void setBreadth(float breadth) {
		this.breadth = breadth;
	}
	
	
	public float area() {
		return areaval = (float)length * breadth;
	}
	
}
